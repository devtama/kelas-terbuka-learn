# Belajar Stack

tumpukan = [1,2,3,4,5,6]
print(f"data sekarang {tumpukan}")
# Menambah data baru
tumpukan.append(7)
print(f"data masuk {7}")
tumpukan.append(8)
print(f"data masuk {8}")
print(f"data sekarang {tumpukan}")

out = tumpukan.pop() # untuk menghapus data paling akhir
print(f"data keluar {out}")
print(f"data sekarang {tumpukan}")